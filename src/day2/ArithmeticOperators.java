package day2;

public class ArithmeticOperators {
public static void main(String[] args) {
	int num1=45;
	int num2=25;
	int sum=num1+num2;
	int diff=num1-num2;
	int mul=num1*num2;
	int div=num1/num2;
	int rem=num1%num2;
	System.out.println("Sum of two numbers : "+sum);
	System.out.println("Difference of two numbers : "+diff);
	System.out.println("Mulitiplication of two numbers : "+mul);
	System.out.println("Division of two numberes : "+div);
	System.out.println("Remainder of two numbers : "+rem);
}
}
