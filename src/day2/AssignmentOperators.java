package day2;

public class AssignmentOperators {
public static void main(String[] args) {
	int a=10;
	System.out.println("Before adding a : "+a);
	a+=20;
	System.out.println("After adding a : "+a);
	a*=6;
	System.out.println("After mulitplying 6 a : "+a);
	a/=10;
	System.out.println("After dividing 10 a : "+a);
	a%=10;
	System.out.println("After dividing 10 a : "+a);
	

}
}
